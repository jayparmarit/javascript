// && and --> 2 condition -> true
// || or -> 1 condition ->true
// ! not -> reverse



// &&

let age = false;

let haslicense = false;

if ( age && haslicense){
    console.log("you can drive");
}


// or

if(age || haslicense){
    console.log("you can drive");
}


// not

if(!haslicense){
    console.log("you can drive")
}
